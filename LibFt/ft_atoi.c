#include "libft.h"

int	ft_atoi(const char	*str)
{
	int	i;
	int	j;
	int	a;
	int	n;

	i = 0;
	j = 0;
	a = 1;
	n = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	while (str[i] == 43 || str[i] == 45)
	{
		if (str[i++] == '-')
			a = -a;
		j++;
	}
	if (j > 1)
		return (0);
	while (str[i] >= 48 && str[i] <= 57)
	{
		n = n * 10 + (str[i] - 48);
		i++;
	}
	return (a * n);
}
