#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n >= 0 && n <= 9)
	{
		ft_putchar_fd((n + '0'), fd);
	}
	if (n > 9 && n <= 2147483647)
	{
		ft_putnbr_fd((n / 10), fd);
		ft_putnbr_fd((n % 10), fd);
	}
	if (n < 0 && n != -2147483648)
	{
		write(1, "-", 1);
		ft_putnbr_fd((n * -1), fd);
	}
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
}
